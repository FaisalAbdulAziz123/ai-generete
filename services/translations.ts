export const t = (lang: string, key: string) => {
  const keys = key.split('.');
  let current = dictionaries[lang] || dictionaries['en'];
  
  for (const k of keys) {
    if (current[k] === undefined) {
      // Fallback to English if key missing in selected language
      current = dictionaries['en']; 
      for (const enK of keys) {
         current = current?.[enK];
      }
      break;
    }
    current = current[k];
  }
  return current || key;
};
